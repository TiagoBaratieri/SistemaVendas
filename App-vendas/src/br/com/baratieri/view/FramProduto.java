    public void listar() throws Exception {
        ProdutoDao dao = new ProdutoDao();
        List<Produto> lista = dao.listaProdutos();
        DefaultTableModel dados = (DefaultTableModel) tabelaProduto.getModel();

        dados.setNumRows(0);

        lista.forEach(item -> {
            dados.addRow(new Object[]{
                item.getId(),
                item.getNome(),
                item.getDescricao(),
                item.getPreco(),
                item.getQuantidadeEstoque(),
                item.getFornecedor().getNome(),});
        });
    }

