    private void btnSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar1ActionPerformed

        Produto obj = new Produto();
        Date data = new Date();
        SimpleDateFormat dataEUA = new SimpleDateFormat("yyyy-MM-dd");

        String dataMySQL = dataEUA.format(data);

        obj.setNome(txtNome.getText());
        obj.setDescricao(txtDesc.getText());
        obj.setPreco(Double.parseDouble(txtPreco.getText()));
        obj.setQuantidadeEstoque(Integer.parseInt(txtQtdEstoque.getText()));
        obj.setDataEntrada(dataMySQL);

        Fornecedor f = new Fornecedor();

        f = (Fornecedor) cbFornecedor.getSelectedItem();

        obj.setFornecedor(f);

        new Utilitarios().limparTela(painel_dados);

        ProdutoDao dao;

        try {
            dao = new ProdutoDao();
            dao.cadastrarProduto(obj);
        } catch (Exception ex) {
            Logger.getLogger(FramFornecedores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvar1ActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String nome = "%" + txtNome.getText() + "%";
            Produto obj = new Produto();
            ProdutoDao dao = new ProdutoDao();

            obj = dao.consultaPorNome(nome);

            cbFornecedor.removeAllItems();

            if (obj.getNome() != null) {

                try {
                    //Exibi os dados do obj nos campos de texto
                    txtCod.setText(String.valueOf(obj.getId()));
                    txtNome.setText(obj.getNome());
                    txtDesc.setText(obj.getDescricao());
                    txtPreco.setText(String.valueOf(obj.getPreco()));
                    txtQtdEstoque.setText(String.valueOf(obj.getQuantidadeEstoque()));

                    Fornecedor f = new Fornecedor();
                    FornecedorDao fdao = new FornecedorDao();

                    f = fdao.consultaPorNome(obj.getFornecedor().getNome());

                    cbFornecedor.getModel().setSelectedItem(f);
                } catch (Exception ex) {
                    Logger.getLogger(FramProduto.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Produto não encontrado!");
            }
        } catch (Exception ex) {
            Logger.getLogger(FramProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

