    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
         // botao calcular total de venda por data
        try {
            
            //Receber a data
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataVenda = LocalDate.parse(txtData.getText(), formato);
            
            double totalVenda;
            
            VendaDao dao = new VendaDao();
            totalVenda = dao.retornaTotalVendaPorData(dataVenda);
            
            txtTotalVenda.setText(String.valueOf(totalVenda));
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Data Inv√°lida.");
                   
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

