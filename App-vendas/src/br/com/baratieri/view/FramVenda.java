    private void jBItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBItemActionPerformed
        try {

            quantidade = Integer.parseInt(txtQuantidade.getText());
            preco = Double.parseDouble(txtPreco.getText());
            
            if (quantidade > 0) {

                subTotal = quantidade * preco;
                total += subTotal;
                txtTotalVendas.setText(String.valueOf(total));

                carrinho = (DefaultTableModel) tabelaItem.getModel();

                carrinho.addRow(new Object[]{
                    txtCod.getText(),
                    txtProduto.getText(),
                    txtQuantidade.getText(),
                    txtPreco.getText(),
                    subTotal
                });
            } else {
                JOptionPane.showMessageDialog(null, "Dados do Produto são Obrigatórios.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Dados do Produto são Obrigatórios.");
        }
    }//GEN-LAST:event_jBItemActionPerformed

