    private void txtCodKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            Produto obj = new Produto();

            try {
                ProdutoDao dao = new ProdutoDao();

                obj = dao.buscaPorCodigo(Integer.parseInt(txtCod.getText()));

                txtProduto.setText(obj.getNome());
                txtDesc.setText(obj.getDescricao());
                txtPreco.setText(String.valueOf(obj.getPreco()));
                txtQuantidade.setText(String.valueOf(obj.getQuantidadeEstoque()));
            } catch (Exception ex) {
                Logger.getLogger(FramVenda.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_txtCodKeyPressed

